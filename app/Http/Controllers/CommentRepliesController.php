    public function createReply(Request $request) {
      $user = Auth::user();

      if ($user->photo) {
        $photo = $user->photo->file;
      } else {
        $photo = '';
      }

      $data = [
        'comment_id' => $request->comment_id,
        'author' => $user->name,
        'email' => $user->email,
        'photo' => $photo,
        'body' => $request->body,
      ];
      
      CommentReply::create($data);
      Session::flash('message', 'Reply saved!');
      return redirect()->back();
    }

